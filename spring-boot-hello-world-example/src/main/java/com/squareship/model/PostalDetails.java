package com.squareship.model;

public class PostalDetails {

	private String status;
	private double distance_in_kilometers;
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public double getDistance_in_kilometers() {
		return distance_in_kilometers;
	}
	public void setDistance_in_kilometers(double distance_in_kilometers) {
		this.distance_in_kilometers = distance_in_kilometers;
	}
	
	
	
}
